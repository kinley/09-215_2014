    private void InActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InActionPerformed

        boolean adm=true;
        boolean cstm=true;
        if(InLogin.getText().equals("root")&&InPassword.getText().equals("root"))
            adm=false;
        if(adm){
            try {
                ResultSet rs = null;
                PreparedStatement pst=null;
                String sql="select * from " + tableCustomers;
                pst=conn.prepareStatement(sql);
                rs = pst.executeQuery();
                while(rs.next()&&cstm){
                    if(InLogin.getText().equals(rs.getString("login"))&&InPassword.getText().equals(rs.getString("password"))) {
                            cstm=false;   
                    }
                }
                rs.close();
                pst.close();
            }
            catch (SQLException ex) {
                Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }            
        }

        if(!adm&&cstm){
                PanelReg.setVisible(false);
                PanelChoose.setVisible(false);
                PanelAdmin.setVisible(true);
                PanelClients.setVisible(false);
        }
        else{
            if(adm&&!cstm){
                    PanelReg.setVisible(false);
                    PanelChoose.setVisible(false);
                    PanelClients.setVisible(true);
                    PanelAdmin.setVisible(false);
            }
            else {
                JOptionPane.showMessageDialog(NewJFrame.this,"Ошибка","Введен не верный логин или пароль.",JOptionPane.WARNING_MESSAGE);
                InPassword.setText("");
                InLogin.setText("");
                return;
            }           
        }

    }//GEN-LAST:event_InActionPerformed

